package datastructures.datatypes.primitive;

public class Main {
    public static void main(String[] args) {
        int myInt = 12345;
        double myDouble = 3.14;
        char myChar = 'M';
        boolean myBoolean = true;

        System.out.println(String.format("MyInt = %d", myInt));
        System.out.println(String.format("MyDouble = %f", myDouble));
        System.out.println(String.format("MyChar = %c", myChar));
        System.out.println(String.format("MyBoolean = %b", myBoolean));
    }
}
